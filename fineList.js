let DB = data.finesData;

/*
   Напишіть свій код тут!
   Як ви бачите функція повертає статичні дані.
   Замість масиву який прописаний хардкодом, вам необхідно реалізувати цю функцію
   так, щоб вона повертала масив відповідно переданому значенню в функцію.
   Саме значення - це "Пошук за номером" або "Пошук за типом штрафу"
   Тип штрафу може бути тільки
   - Перевищення швидкості
   - Невірне паркування
   - Їзда у не тверезому стані
   */

   function searchFines(searchKey) {
    // Створюємо порожній масив для зберігання результатів пошуку
    let searchResults = [];


    // Перевіряємо, чи передане значення searchKey не є порожнім рядком
    if (searchKey.trim() === '') {
        return data.finesData; // Повертаємо весь список штрафів, якщо searchKey порожній
    }


    // Перебираємо всі штрафи в базі даних
    for (let i = 0; i < DB.length; i++) {
        // Перевіряємо, чи номер штрафу або тип штрафу відповідають переданому значенню searchKey
        if (DB[i].номер === searchKey || DB[i].тип === searchKey) {
            // Якщо збігається, додаємо штраф до масиву searchResults
            searchResults.push(DB[i]);
        }
    }


    // Повертаємо список штрафів, якщо нічого не знайдено
    if (searchResults.length === 0) {
        alert("Штраф не знайдено");
        return data.finesData;
    }

    
    // Повертаємо масив з результатами пошуку
    return searchResults;
}

